<script>
import axios from 'axios'

export default {
    data () {
        return {
            username: '',
            password: ''
        }
    },

    methods: {
        async signin () {
            let data = await axios.post('http://localhost:3000/api/v1.0/users/signin', data={
                username: this.username,
                password: this.password
            })

            let status = data.status;

            if(status == 200){
                this.$nuxt.$router.replace({ path: '/' })
                this.$store.commit('userCoinUpdate', data.data.coin);
            }else{
                alert('로그인 실패')
            }

        }
    }
}
</script>


